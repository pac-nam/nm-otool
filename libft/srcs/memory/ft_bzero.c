#include <stdlib.h>

/*
**	ft_bzero write n 0 from s.
*/

void		ft_bzero(void *s, size_t n)
{
	unsigned char	*s2;

	n++;
	s2 = s;
	while (n > 1)
		s2[--n - 1] = 0;
}