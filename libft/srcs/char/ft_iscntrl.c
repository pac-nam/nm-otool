/*
**	ft_iscntrl return 1 if the integer passed is a control ascii code,
**	0 elseway.
*/

int		ft_iscntrl(int c)
{
	if ((c >= 0 && c < 8)
			|| (c > 9 && c < 18)
			|| (c > 19 && c < 28)
			|| (c > 29 && c < 38)
			|| (c == 177))
		return (1);
	return (0);
}
